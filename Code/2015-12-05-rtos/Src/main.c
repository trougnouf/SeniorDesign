static void LED1Thread(void const *argument)
{
	unsigned char LED1state = 0;
	for(;;)
	{
		//HAL_GPIO_TogglePin(GPIOI, GPIO_PIN_3);
		HAL_GPIO_WritePin(GPIOI, GPIO_PIN_3, LED1state);
		osDelay(10);
		LED1state = 1;
		HAL_GPIO_WritePin(GPIOI, GPIO_PIN_3, LED1state);
		osDelay(1);
		LED1state = 0;
	}
}

static void LED2Thread(void const *argument)
{
	for(;;)
	{
		HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_7);
		printf("Toggled LED2");
	}
}

